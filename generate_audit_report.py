# ─── HEADER / FOOTER ──────────────────────────────────────────────────────────
class HeaderFooterCanvas(canvas.Canvas):
    def __init__(self, *args, **kwargs):
        self._saved_page_states = []
        super().__init__(*args, **kwargs)

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self._draw_header_footer(num_pages)
            super().showPage()
        super().save()

    def _draw_header_footer(self, page_count):
        page_num = self._pageNumber
        w, h = A4

        # Header bar
        self.setFillColor(DARK_BG)
        self.rect(0, h - 22*mm, w, 22*mm, fill=1, stroke=0)

        # Header left text
        self.setFillColor(colors.white)
        self.setFont('Helvetica-Bold', 9)
        self.drawString(15*mm, h - 13*mm, 'CAKE DESIGN ACADEMY')
        self.setFont('Helvetica', 7.5)
        self.setFillColor(ACCENT_GOLD)
        self.drawString(15*mm, h - 18*mm, 'Complete Application Audit Report · 2026')

        # Header right text
        self.setFillColor(colors.white)
        self.setFont('Helvetica', 7.5)
        self.drawRightString(w - 15*mm, h - 13*mm, 'CONFIDENTIAL')
        self.setFillColor(ACCENT_GRAY)
        self.drawRightString(w - 15*mm, h - 18*mm, f'Page {page_num} of {page_count}')

        # Gold accent line
        self.setStrokeColor(ACCENT_GOLD)
        self.setLineWidth(1.5)
        self.line(0, h - 22*mm, w, h - 22*mm)

        # Footer
        self.setFillColor(LIGHT_BG)
        self.rect(0, 0, w, 12*mm, fill=1, stroke=0)
        self.setStrokeColor(BORDER_LIGHT)
        self.setLineWidth(0.5)
        self.line(0, 12*mm, w, 12*mm)

        self.setFillColor(TEXT_GRAY)
        self.setFont('Helvetica', 6.5)
        self.drawString(15*mm, 4.5*mm, 'Generated by QA Audit System · Cake Design Academy · All rights reserved')
        self.drawRightString(w - 15*mm, 4.5*mm, f'{datetime.date.today().strftime("%B %d, %Y")}')


