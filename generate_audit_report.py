# ─── FEATURE AUDIT TABLE ──────────────────────────────────────────────────────
def make_feature_table(data_rows, styles):
    """
    data_rows: list of (feature, module, status, severity, description, issue)
    """
    headers = ['Feature', 'Module', 'Status', 'Severity', 'Notes / Issue']
    header_row = [Paragraph(h, styles['table_header']) for h in headers]

    rows = [header_row]
    for feat, mod, sta, sev, note in data_rows:
        severity_colors = {
            'CRITICAL': STATUS_CRIT, 'HIGH': STATUS_HIGH,
            'MEDIUM': STATUS_MED, 'LOW': STATUS_LOW, 'INFO': STATUS_OK,
        }
        sev_style = ParagraphStyle('_sv', fontName='Helvetica-Bold', fontSize=7.5,
                                   textColor=severity_colors.get(sev, TEXT_GRAY), alignment=TA_CENTER)
        rows.append([
            Paragraph(feat, styles['table_cell_small']),
            Paragraph(mod, styles['table_cell_small']),
            status_cell(sta),
            Paragraph(sev, sev_style),
            Paragraph(note, styles['table_cell_small']),
        ])

    col_w = [44*mm, 28*mm, 30*mm, 18*mm, 58*mm]
    t = Table(rows, colWidths=col_w, repeatRows=1)

    style_cmds = [
        ('BACKGROUND', (0, 0), (-1, 0), ACCENT_BLUE),
        ('GRID', (0, 0), (-1, -1), 0.3, BORDER_LIGHT),
        ('ROWBACKGROUNDS', (0, 1), (-1, -1), [colors.white, colors.HexColor('#F8F9FA')]),
        ('TOPPADDING', (0, 0), (-1, -1), 5),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 5),
        ('LEFTPADDING', (0, 0), (-1, -1), 5),
        ('RIGHTPADDING', (0, 0), (-1, -1), 5),
        ('VALIGN', (0, 0), (-1, -1), 'TOP'),
        ('ALIGN', (2, 0), (3, -1), 'CENTER'),
        ('FONTSIZE', (0, 0), (-1, -1), 8),
    ]
    t.setStyle(TableStyle(style_cmds))
    return t


