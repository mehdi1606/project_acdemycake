def severity_badge(text, styles_dict):
    """Color-coded severity text."""
    color_map = {
        'CRITICAL': STATUS_CRIT,
        'HIGH': STATUS_HIGH,
        'MEDIUM': STATUS_MED,
        'LOW': STATUS_LOW,
        'INFO': STATUS_OK,
    }
    bg = color_map.get(text, STATUS_LOW)
    style = ParagraphStyle('_badge', fontName='Helvetica-Bold', fontSize=7.5,
                           textColor=colors.white, alignment=TA_CENTER)
    data = [[Paragraph(text, style)]]
    t = Table(data, colWidths=[18*mm])
    t.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, -1), bg),
        ('TOPPADDING', (0, 0), (-1, -1), 2),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 2),
        ('LEFTPADDING', (0, 0), (-1, -1), 3),
        ('RIGHTPADDING', (0, 0), (-1, -1), 3),
    ]))
    return t


def status_cell(text):
    """Status icon cell."""
    icons = {
        'Working':       ('✅ Working',       STATUS_OK),
        'Partial':       ('⚠️ Partial',        STATUS_HIGH),
        'Broken':        ('❌ Broken',         STATUS_CRIT),
        'Not Connected': ('🔌 Not Connected',  ACCENT_GRAY),
        'UI Only':       ('🎨 UI Only',        STATUS_MED),
    }
    label, clr = icons.get(text, (text, TEXT_GRAY))
    style = ParagraphStyle('_sc', fontName='Helvetica-Bold', fontSize=7.5,
                           textColor=clr)
    return Paragraph(label, style)


